namespace DnDAssistant.Core
{
    /// <summary>
    /// Default class for Buttons
    /// </summary>
    public enum Buttons
    {
        /// <summary>
        /// Show no buttons
        /// </summary>
        None = 0,

        /// <summary>
        /// Show an ok button
        /// </summary>
        Ok = 1,

        /// <summary>
        /// Show Yes/No buttons
        /// </summary>
        YesNo = 2,

        /// <summary>
        /// Show Yes/No/Cancel buttons
        /// </summary>
        YesNoCancel = 3,
    }
}
