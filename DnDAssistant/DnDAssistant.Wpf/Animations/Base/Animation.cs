        None = 0,

        /// <summary>
        /// Slide and Fade animation from top to bottom
        /// </summary>
        SlideAndFadeInFromTop = 1,

        /// <summary>
        /// Slide and Fade animation from bottom to top
        /// </summary>
        SlideAndFadeOutToTop = 2,
    }
}
